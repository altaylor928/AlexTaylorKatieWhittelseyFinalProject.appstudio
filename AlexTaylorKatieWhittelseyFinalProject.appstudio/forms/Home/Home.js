function calsPerDay() {

  var age = inptAge.value
  var height = inptHeight.value * 2.54
  var weight = inptWeight.value / 2.2
  var result = 0
  if (find("Male").checked) 
    result = 66.47 + (13.75 * weight) + (5.0 * height - (6.75 * age))
  else if (find("Female").checked)
    result = 665.09 + (9.56 * weight) + (1.84 * height - (4.67 * age))

lblCalories = (`Based on the information above, you need to consume: ${result} calories`)
}




